    public void insertDat(String nombre, String dni, String direccion, int telefono, String email, Date fecha) {
        String query = "INSERT INTO conductores (NombreConductor,DniConductor,DireccionConductor,TelefonoConductor,EmailConductor,FechaContrato) VALUES (?,?,?,?,?,?)";
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement enviarConsultas = cn.prepareStatement(query);
            enviarConsultas.setString(1, nombre);
            enviarConsultas.setString(2, dni);
            enviarConsultas.setString(3, direccion);
            enviarConsultas.setInt(4, telefono);
            enviarConsultas.setString(5, email);
            enviarConsultas.setDate(6, fecha);
            enviarConsultas.executeUpdate();
            cn.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al insertar datos: " + e.getMessage());
        }
    }

