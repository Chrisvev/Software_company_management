    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        nombre = fieldnombre.getText();
        dni = fielddni.getText();
        direccion = fielddireccion.getText();
        telefono = fieldtelefono.getText();
        email = fieldemail.getText();

        if (datechooser.getDate() != null) {
            java.util.Date utilDate = datechooser.getDate();
            long time = utilDate.getTime();
            java.sql.Date sqlDate = new java.sql.Date(time);
            fecha = sqlDate;

        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fecha");
        }
        if (fielddni.getText().length() != 9) {
            JOptionPane.showMessageDialog(null, "Campo DNI invalido");
        } else if (nombre.isEmpty() || dni.isEmpty() || direccion.isEmpty() || fieldtelefono.getText().isEmpty() || email.isEmpty() || fecha == null) {

            JOptionPane.showMessageDialog(null, "Debes rellenar los campos vacios");
        } else {
            insertDat(nombre, dni, direccion, Integer.parseInt(fieldtelefono.getText()), email, fecha);
            notificacion();
            JOptionPane.showMessageDialog(null, "Datos guardados", "", JOptionPane.INFORMATION_MESSAGE);
        }


    }//GEN-LAST:event_BotonGuardarActionPerformed

