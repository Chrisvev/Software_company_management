    public void insertDat(String nombre, String apellidos, String dni, String direccion, int telefono, String email, String sexo) {
        String query = "INSERT INTO clientes(NombreCliente,ApellidoCliente,DniCliente,DireccionCliente,TelefonoCliente,EmailCliente,Sexo) VALUES (?,?,?,?,?,?,?)";
        try {

            Connection cn = Conexion.conectar();
            PreparedStatement enviarConsultas = cn.prepareStatement(query);
            enviarConsultas.setString(1, nombre);
            enviarConsultas.setString(2, apellidos);
            enviarConsultas.setString(3, dni);
            enviarConsultas.setString(4, direccion);
            enviarConsultas.setInt(5, telefono);
            enviarConsultas.setString(6, email);
            enviarConsultas.setString(7, sexo);
            enviarConsultas.executeUpdate();
            cn.close();

        } catch (SQLException e) {
            System.out.println("Error" + e);
        }
    }

    @Override
    public void notificacion() {
        try {
            // Carga el archivo de sonido
            AudioInputStream audioStream = AudioSystem.getAudioInputStream(sonido);

            // Obtiene el formato de audio
            AudioFormat format = audioStream.getFormat();

            // Crea una fuente de datos de audio
            DataLine.Info info = new DataLine.Info(Clip.class, format);

            // Abre el clip de audio
            Clip clip = (Clip) AudioSystem.getLine(info);
            clip.open(audioStream);

            // Agrega un LineListener para detectar cuando finaliza la reproducción del clip
            clip.addLineListener(new LineListener() {
                @Override
                public void update(LineEvent event) {
                    if (event.getType() == LineEvent.Type.STOP) {
                    }
                }
            });

            // Reproduce el sonido
            clip.start();
        } catch (UnsupportedAudioFileException | LineUnavailableException | IOException e) {
            e.printStackTrace();
        }
    }

