    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        notificacion();
        nombre = texfieldnombre.getText();
        apellidos = textfieldapellidos.getText();
        dni = fielddni.getText();
        direccion = texfielddireccion.getText();
        try {
            ResultSet resultado = null;
            Connection cn = Conexion.conectar();
            String verificar = "SELECT * FROM clientes WHERE DniCliente = ?";
            PreparedStatement enviarVerificacion = cn.prepareStatement(verificar);
            enviarVerificacion.setString(1, dni);
            resultado = enviarVerificacion.executeQuery();
            if (!resultado.next()) {
                if (checkhombre.isSelected() && checkmujer.isSelected()) {
                    JOptionPane.showMessageDialog(null, "Solo puedes escoger un solo tipo de sexo");
                } else {
                    if (!checkmujer.isSelected() && !checkhombre.isSelected()) {
                        JOptionPane.showMessageDialog(null, "Debes seleccionar al menos un sexo");
                    } else {
                        verificarCheck();
                        email = texfieldemail.getText();
                        if (nombre.isEmpty() || apellidos.isEmpty() || dni.isEmpty() || direccion.isEmpty() || texfieldtelefono.getText().length() == 0 || email.isEmpty()) {
                            JOptionPane.showMessageDialog(null, "Debes rellenar los campos vacios");
                        } else if (fielddni.getText().length() != 9) {

                            JOptionPane.showMessageDialog(null, "Campo DNI invalido");
                        } else {
                            int opcion = JOptionPane.showConfirmDialog(null, "¿Estas seguro?");
                            if (opcion == JOptionPane.YES_OPTION) {
                                insertDat(nombre, apellidos, dni, direccion, Integer.parseInt(texfieldtelefono.getText()), email, sexo);
                                JOptionPane.showMessageDialog(null, "Datos guardados");
                            }
                        }
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "DNI ya registrado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "La base de datos no esta activa");
        }


    }//GEN-LAST:event_BotonGuardarActionPerformed

